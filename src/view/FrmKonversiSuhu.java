    private void btnKonversiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKonversiActionPerformed
     try {
        // 1️⃣ Ambil nilai dari text field input
        double input = Double.parseDouble(txtInput.getText());
        
        // 2️⃣ Ambil pilihan skala asal
        String asal = cmbAsal.getSelectedItem().toString();
        
        // 3️⃣ Variabel hasil dan tujuan
        double hasil = 0;
        String tujuan = "";

        // 4️⃣ Tentukan skala tujuan berdasarkan radio button yang dipilih
        if (rbC.isSelected()) {
            tujuan = "Celsius";
            hasil = KonversiSuhu.keCelsius(input, asal);
        } else if (rbF.isSelected()) {
            tujuan = "Fahrenheit";
            hasil = KonversiSuhu.keFahrenheit(input, asal);
        } else if (rbR.isSelected()) {
            tujuan = "Reamur";
            hasil = KonversiSuhu.keReamur(input, asal);
        } else if (rbK.isSelected()) {
            tujuan = "Kelvin";
            hasil = KonversiSuhu.keKelvin(input, asal);
        } else {
            JOptionPane.showMessageDialog(this, "Pilih skala tujuan terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 5️⃣ Tampilkan hasil pada text field hasil
        txtHasil.setText(String.format("%.2f %s", hasil, tujuan));

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan angka yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
    }     // TODO add your handling code here:
    }//GEN-LAST:event_btnKonversiActionPerformed

    private void txtInputKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtInputKeyTyped
    char c = evt.getKeyChar();
    String currentText = txtInput.getText();
    
    if (!Character.isDigit(c) && c != '.' && c != KeyEvent.VK_BACK_SPACE && c != '-') {
        evt.consume();
        return;
    }

    if (c == '-') {
        // Minus hanya boleh di posisi pertama
        if (txtInput.getCaretPosition() != 0 || currentText.contains("-")) {
            evt.consume();
            return;
        }
    }
    
    // Validasi untuk titik desimal
    if (c == '.' && currentText.contains(".")) {
        evt.consume();
        return;
    }
    }//GEN-LAST:event_txtInputKeyTyped
